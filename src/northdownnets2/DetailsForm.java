    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        //This boolean variable ensures data can only be updated if no fields are empty
        boolean emptyFields = false;
        
        //If field is empty emptyFields variable is set to true
        if (txtUpdateUsername.getText().trim().equals("")) {
            lblUsernameError.setText("Username field is empty");
            emptyFields = true;
        }

        if (txtUpdatePassword.getText().trim().equals("")) {
            lblPasswordError.setText("Password field is empty");
            emptyFields = true;
        }

        if (txtUpdateFirstname.getText().trim().equals("")) {
            lblFirstnameError.setText("Firstname field is empty");
            emptyFields = true;
        }

        if (txtUpdateLastname.getText().trim().equals("")) {
            lblLastnameError.setText("Lastname field is empty");
            emptyFields = true;
        }

        if (txtUpdateDateOfBirth.getText().trim().equals("")) {
            lblDoBError.setText("Date Of Birth field is empty");
            //emptyFields = true;
        }

        if (txtUpdatePhoneNumber.getText().trim().equals("")) {
            lblPhoneNoError.setText("Phone Number field is empty");
            emptyFields = true;
        }

        if (txtUpdateEmail.getText().trim().equals("")) {
            lblEmailError.setText("Email field is empty");
            emptyFields = true;
        }

        if (txtUpdateSecurityQuestion.getText().trim().equals("")) {
            lblSecurityQuestionError.setText("Security Question field is empty");
            emptyFields = true;
        }

        if (txtUpdateSecurityAnswer.getText().trim().equals("")) {
            lblSecurityAnswerError.setText("Security Answer field is empty");
            emptyFields = true;

        }

        //This boolean variable ensures data can only be updated if text fields are in the correct format
        boolean validationCheck = false;
        //If text does not adhere to the defined validation the variable validationCheck is set to true
        
        //The text field is converted to a character array so that each individual character can be checked to ensure there are no numbers in the field
        char[] firstnameCheck = txtUpdateFirstname.getText().trim().toCharArray();
        for (int i = 0; i < firstnameCheck.length; i++) {
            if (Character.isDigit(firstnameCheck[i])) {
                JOptionPane.showMessageDialog(null, "Firstname can not contain numbers");
                validationCheck = true;
                break;
            }
        }
        
        //The text field is converted to a character array so that each individual character can be checked to ensure there are no numbers in the field
        char[] lastnameCheck = txtUpdateLastname.getText().trim().toCharArray();
        for (int i = 0; i < lastnameCheck.length; i++) {
            if (Character.isDigit(lastnameCheck[i])) {
                JOptionPane.showMessageDialog(null, "Lastname can not contain numbers");
                validationCheck = true;
                break;
            }
        }
        
        //The text field is converted to a character array to ensure that the length of the field is exactly 11 digits
        char[] phoneNoCheck = txtUpdatePhoneNumber.getText().trim().toCharArray();
        if(phoneNoCheck.length != 11) {
            JOptionPane.showMessageDialog(null, "Phone number must be 11 numbers");
        }
        
        //This character array is also used so that each individual character can be checked to ensure there are no letters in the field
        for (int i = 0; i < phoneNoCheck.length; i++) {
            if(Character.isLetter(phoneNoCheck[i])) {
            JOptionPane.showMessageDialog(null, "Phone number can not contain letters");
            validationCheck = true;
            break;
            }
        }
        
        
        
        //The string regex is used to ensure th email field follows the correct format
        String regex = "^(.+)@(.+)$";
        Pattern pattern = Pattern.compile(regex);
        Matcher m = pattern.matcher(txtUpdateEmail.getText().trim());  
        //Matching the particular pattern against regular expression  
        boolean matches = m.matches();  
        if(matches == false){
            JOptionPane.showMessageDialog(null, "Email must follow format: characters + @ + characters + .");
            validationCheck = true;
        }
        
        if (emptyFields == false && validationCheck == false) {
            updateInfo();
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

