    public void setTextInfo() {
        //Each text field is populated with the users information from the database
        txtUpdateUsername.setText(thisCustomer.getUsername());
        txtUpdatePassword.setText(thisCustomer.getPassword());
        txtUpdateFirstname.setText(thisCustomer.getFirstname());
        txtUpdateLastname.setText(thisCustomer.getLastname());
        txtUpdateDateOfBirth.setValue(thisCustomer.getDateOfBirth());
        txtUpdateEmail.setText(thisCustomer.getEmail());
        txtUpdatePhoneNumber.setText(thisCustomer.getPhoneNumber());
        txtUpdateSecurityQuestion.setText(thisCustomer.getSecurityQuestion());
        txtUpdateSecurityAnswer.setText(thisCustomer.getSecurityAnswer());
    }
    
    public void checkForGuest() {
        //If the user is a guest then they can not amend their information
        //All text fields are set to unEditable
        if (txtUpdateUsername.getText().equals("guest"))
            {
                btnUpdate.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Cannot amend information if signed in as a guest.");
                txtUpdateUsername.setEditable(false);
                txtUpdatePassword.setEditable(false);
                txtUpdateFirstname.setEditable(false);
                txtUpdateLastname.setEditable(false);
                txtUpdateDateOfBirth.setEditable(false);
                txtUpdateEmail.setEditable(false);
                txtUpdatePhoneNumber.setEditable(false);
                txtUpdateSecurityQuestion.setEditable(false);
                txtUpdateSecurityAnswer.setEditable(false);
            }
    }
    
    public void updateInfo() {
        //The amended user information is updated if validation checks are adhered to
        thisCustomer.setUsername(txtUpdateUsername.getText());
        thisCustomer.setPassword(txtUpdatePassword.getText());
        thisCustomer.setFirstname(txtUpdateFirstname.getText());
        thisCustomer.setLastname(txtUpdateLastname.getText());
        thisCustomer.setDateOfBirth((java.util.Date) txtUpdateDateOfBirth.getValue());
        thisCustomer.setEmail(txtUpdateEmail.getText());
        thisCustomer.setPhoneNumber(txtUpdatePhoneNumber.getText());
        thisCustomer.setSecurityQuestion(txtUpdateSecurityQuestion.getText());
        thisCustomer.setSecurityAnswer(txtUpdateSecurityAnswer.getText());
        
        //The customers details are updated in the database
        custList.updateCustomer(thisCustomer);
        
        //The form is then closed
        this.dispose();
    }
    
   

    
